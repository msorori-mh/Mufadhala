export function getMotivationalMessage(collegeName: string | null, avgScore: number): string {
  const pool = [...generalMessages];

  if (collegeName) {
    const name = collegeName.toLowerCase();
    if (name.includes("طب")) pool.push(...medicineMessages);
    else if (name.includes("هندسة")) pool.push(...engineeringMessages);
    else if (name.includes("حاسوب") || name.includes("حاسب") || name.includes("computer")) pool.push(...computerMessages);
  }

  if (avgScore >= 90) pool.push(...highPerformanceMessages);
  else if (avgScore > 0 && avgScore < 50) pool.push(...lowPerformanceMessages);

  return pool[Math.floor(Math.random() * pool.length)];
}
